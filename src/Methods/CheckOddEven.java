package Methods;

public class CheckOddEven {
    public static void main(String[] args) {


        int number = 79;       // Set the value of "number" here!
        System.out.println("The number is " + number);
        if ( 79%2==0 ) {
            System.out.println("The given number is  even " );   // even number
        } else {
            System.out.println("The given number is odd  " );   // odd number
        }
        System.out.println(" Bye ");









    }
}
