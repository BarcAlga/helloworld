package DevXDragons;

public class SwapNumbers {
    public static void main(String[] args) {

        int number1 = 15 , number2 = 23 ;

        System.out.println("First integer is :  " + number1);
        System.out.println("Second integer is :  " + number2);

        int inputNum = number1;

        number1 = number2 ;

        number2 = inputNum;


        System.out.println("First integer :  " + number1);
        System.out.println("Second integer :  " + number2);








    }
}
